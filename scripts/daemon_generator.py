for name, (grid, _) in SPECIES.items():
    assert len(grid) == H, f"{name}: {len(grid)} rows"
    for i, row in enumerate(grid):
        assert len(row) == W, f"{name} row {i}: {len(row)} cols"


def make_daemon(species, variant=None, rng=random):
    grid, ramps = SPECIES[species]
    ramp = ramps[variant] if variant is not None else rng.choice(ramps)
    img = Image.new("RGBA", (W, H), (0, 0, 0, 0))
    paint(img, grid, ramp)
    add_outline(img)
    return img


def preview_grid(scale=6):
    """One row per species, one column per natural color variant."""
    pad = 6
    cell = W + pad
    cols = max(len(r) for _, r in SPECIES.values())
    rows = len(SPECIES)
    sheet = Image.new("RGBA", (cols * cell + pad, rows * cell + pad),
                      (58, 58, 70, 255))
    for r, (name, (grid, ramps)) in enumerate(SPECIES.items()):
        for c, _ in enumerate(ramps):
            spr = make_daemon(name, variant=c)
            sheet.paste(spr, (pad + c * cell, pad + r * cell), spr)
    return sheet.resize((sheet.width * scale, sheet.height * scale),
                        Image.NEAREST)


