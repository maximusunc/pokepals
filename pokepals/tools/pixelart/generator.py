B32 = "." * 32

def _arm_maps(rows15_21, r22, r23, left=True):
    grid = [B32] * 15
    for ch in rows15_21:
        grid.append("." * 7 + ch + "." * 22 if left else "." * 22 + ch + "." * 7)
    grid.append("." * 7 + r22 + "." * 22 if left else "." * 22 + r22 + "." * 7)
    grid.append("." * 7 + r23 + "." * 22 if left else "." * 22 + r23 + "." * 7)
    grid += [B32] * 8
    return grid

