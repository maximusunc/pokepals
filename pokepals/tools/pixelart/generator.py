def paint(img, layer, ramp):
    if layer is None:
        return
    px = img.load()
    for y, row in enumerate(layer):
        for x, ch in enumerate(row):
            if ch == '.':
                continue
            px[x, y] = EYE if ch == 'E' else ramp[int(ch) - 1] + (255,)


def add_outline(img):
    px = img.load()
    solid = {(x, y) for x in range(W) for y in range(H) if px[x, y][3] > 0}
    for (x, y) in list(solid):
        for dx, dy in ((1, 0), (-1, 0), (0, 1), (0, -1)):
            nx, ny = x + dx, y + dy
            if 0 <= nx < W and 0 <= ny < H and (nx, ny) not in solid:
                px[nx, ny] = OUTLINE


def make_character(hair=None, accessory=None, skin=None, hair_c=None,
                   shirt_c=None, pants_c=None, shoes_c=None,
                   garment=None, rng=random):
    garment = garment or rng.choice(list(GARMENTS))
    g = GARMENTS[garment]
    hair = hair or rng.choice(list(HAIR_STYLES))
    accessory = accessory or rng.choice(list(ACCESSORIES))
    skin = skin or rng.choice(SKIN_RAMPS)
    hair_c = hair_c or rng.choice(HAIR_RAMPS)
    shirt_c = shirt_c or rng.choice(CLOTH_RAMPS)
    pants_c = pants_c or rng.choice(CLOTH_RAMPS)
    shoes_c = shoes_c or rng.choice(CLOTH_RAMPS)
    acc_c = ((25, 20, 25),) * 3 if accessory == "glasses" else rng.choice(CLOTH_RAMPS)

    img = Image.new("RGBA", (W, H), (0, 0, 0, 0))
    paint(img, BODY, skin)
    paint(img, PANTS, pants_c)
    paint(img, SHOES, shoes_c)
    paint(img, ARM_L, skin)
    paint(img, ARM_R, skin)
    paint(img, g["torso"], shirt_c)
    paint(img, g["arm_l"], shirt_c)
    paint(img, g["arm_r"], shirt_c)
    paint(img, HAIR_STYLES[hair], hair_c)
    paint(img, ACCESSORIES[accessory], acc_c)
    add_outline(img)
    return img


def preview_grid(cols=6, rows=4, scale=6, seed=42):
    rng = random.Random(seed)
    pad = 6
    cell = W + pad
    sheet = Image.new("RGBA", (cols * cell + pad, rows * cell + pad),
                      (58, 58, 70, 255))
    for r in range(rows):
        for c in range(cols):
            spr = make_character(rng=rng)
            sheet.paste(spr, (pad + c * cell, pad + r * cell), spr)
    return sheet.resize((sheet.width * scale, sheet.height * scale),
                        Image.NEAREST)


